
public class MathematicalOperatiors {

	
	
	public static void main(String[] args) {
		
		
		//define variables
		
		int x = 5;
		int y = 3;
		int z;
		
		//simple add
		z = x+y;
		System.out.println(z);
		//simple sub
		z = x-y;
		//simple multi
		z = x * y;
		System.out.println(z);
		
	    //sim div
		z = x/y;
		System.out.println(z);
		
		//sim mod
		z = x%y;
		System.out.println(z);
		
		
		
		//float vs. int
		float f = 3.6f;
		int i = 22;
		int result = (int)f + i;
		
		System.out.println(result);
		
	}
}
